var builder = WebApplication.CreateBuilder(args);
// aggiungi AddControllers
builder.Services.AddControllers();

var app = builder.Build();

// aggiungi UseStaticFiles, UseRouting e MapControllers
app.UseStaticFiles();
app.UseRouting();
app.MapControllers();

app.Run();
